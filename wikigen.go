func buildBundle(cfg config, rel string, summaries map[string]string) dirBundle {
	abs := filepath.Join(cfg.repoRoot, rel)
	entries, _ := os.ReadDir(abs)

	b := dirBundle{Path: rel}

	for _, e := range entries {
		if e.IsDir() {
			childRel := filepath.Join(rel, e.Name())
			if s, ok := summaries[childRel]; ok {
				b.ChildSummaries = append(b.ChildSummaries, s)
				b.ChildDirs = append(b.ChildDirs, e.Name())
			}
			continue
		}
		name := e.Name()
		if !isEligibleFile(name) {
			continue
		}
		lang := detectLanguage(name)
		if lang == "" {
			continue
		}
		content, err := readFileContent(filepath.Join(abs, name))
		if err != nil || content == "" {
			continue
		}
		b.Files = append(b.Files, fileEntry{Name: name, Language: lang, Content: content})
	}
	sort.Slice(b.Files, func(i, j int) bool {
		return b.Files[i].Name < b.Files[j].Name
	})
	return b
}

func isEligibleFile(name string) bool {
	if skipFiles[name] || name == "SUMMARY.md" || name == manifestFile {
		return false
	}
	if detectLanguage(name) == "" {
		return false
	}
	return true
}

func detectLanguage(name string) string {
	for prefix, lang := range specialFiles {
		if name == prefix || strings.HasPrefix(name, prefix+".") {
			return lang
		}
	}
	ext := filepath.Ext(name)
	return extLang[ext]
}

func readFileContent(path string) (string, error) {
	info, err := os.Stat(path)
	if err != nil {
		return "", err
	}
	if info.Size() > maxFileBytes {
		return "", nil
	}

	data, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}

	check := data
	if len(check) > 512 {
		check = check[:512]
	}
	if bytes.ContainsRune(check, 0) {
		return "", nil
	}

	header := string(check)
	if strings.Contains(header, "Code generated") || strings.Contains(header, "DO NOT EDIT") {
		return "", nil
	}

	lines := strings.SplitN(string(data), "\n", maxFileLines+1)
	if len(lines) > maxFileLines {
		lines = lines[:maxFileLines]
		lines = append(lines, fmt.Sprintf("\n... (truncated at %d lines)", maxFileLines))
	}
	return strings.Join(lines, "\n"), nil
}

