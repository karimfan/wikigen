func rewriteLinks(cfg config, dirRel string, summary string) string {
	if cfg.baseURL != "" {
		return linkPattern.ReplaceAllStringFunc(summary, func(match string) string {
			parts := linkPattern.FindStringSubmatch(match)
			if len(parts) != 3 {
				return match
			}
			label, file := parts[1], parts[2]
			return fmt.Sprintf("[%s](%s/%s/%s)", label, cfg.baseURL, dirRel, file)
		})
	}
	// No base URL: replace links with inline code.
	return linkPattern.ReplaceAllStringFunc(summary, func(match string) string {
		parts := linkPattern.FindStringSubmatch(match)
		if len(parts) != 3 {
			return match
		}
		return "`" + parts[1] + "`"
	})
}

