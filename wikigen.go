// wikiSection is the marker used to delimit the wikigen-managed block inside
// existing CLAUDE.md / AGENTS.md files.
const (
	wikiSectionStart = "<!-- wikigen:start -->"
	wikiSectionEnd   = "<!-- wikigen:end -->"
)

// writeSkillFiles writes wiki navigation instructions into CLAUDE.md and
// AGENTS.md in the repo root. If a file already exists (user-authored), the
// wiki section is appended or updated in place between marker comments. If
// the file doesn't exist, it is created with the full template.
func writeSkillFiles(cfg config) {
	// Compute wiki path relative to the repo root.
	wikiRel, err := filepath.Rel(cfg.repoRoot, filepath.Join(cfg.wikiRoot, "wiki.md"))
	if err != nil {
		wikiRel = filepath.Join(cfg.wikiRoot, "wiki.md") // fall back to absolute
	}

	for _, name := range []string{"templates/CLAUDE.md", "templates/AGENTS.md"} {
		data, err := skillTemplates.ReadFile(name)
		if err != nil {
			continue
		}

		// Replace placeholders with actual wiki paths.
		wikiDir := filepath.Dir(wikiRel)
		if wikiDir == "." {
			wikiDir = ""
		}
		section := strings.ReplaceAll(string(data), "{{WIKI_PATH}}", wikiRel)
		// When wiki is in repo root, {{WIKI_DIR}}/ should collapse to empty
		// so paths like "{{WIKI_DIR}}/cmd/SUMMARY.md" become "cmd/SUMMARY.md".
		if wikiDir == "" {
			section = strings.ReplaceAll(section, "{{WIKI_DIR}}/", "")
		} else {
			section = strings.ReplaceAll(section, "{{WIKI_DIR}}", wikiDir)
		}
		wrappedSection := fmt.Sprintf("%s\n%s\n%s", wikiSectionStart, strings.TrimSpace(section), wikiSectionEnd)

		outName := filepath.Base(name)
		outPath := filepath.Join(cfg.repoRoot, outName)

		existing, err := os.ReadFile(outPath)
		if err != nil {
			// File doesn't exist — create it with just our section.
			if err := os.WriteFile(outPath, []byte(wrappedSection+"\n"), 0644); err != nil {
				fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", outPath, err)
			}
			continue
		}

		content := string(existing)
		startIdx := strings.Index(content, wikiSectionStart)
		endIdx := strings.Index(content, wikiSectionEnd)

		if startIdx >= 0 && endIdx >= 0 && endIdx > startIdx {
			// Replace existing wikigen section in place.
			updated := content[:startIdx] + wrappedSection + content[endIdx+len(wikiSectionEnd):]
			if err := os.WriteFile(outPath, []byte(updated), 0644); err != nil {
				fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", outPath, err)
			}
		} else {
			// Append the wiki section to the existing file.
			separator := "\n\n"
			if strings.HasSuffix(content, "\n\n") {
				separator = ""
			} else if strings.HasSuffix(content, "\n") {
				separator = "\n"
			}
			updated := content + separator + wrappedSection + "\n"
			if err := os.WriteFile(outPath, []byte(updated), 0644); err != nil {
				fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", outPath, err)
			}
		}
	}
}

