func doClean(cfg config) {
	removed := 0
	filepath.Walk(cfg.wikiRoot, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return nil
		}
		if info.IsDir() {
			return nil
		}
		name := info.Name()
		if name == "SUMMARY.md" || name == "SUMMARY.html" {
			if name == "SUMMARY.html" {
				os.Remove(path)
				fmt.Fprintf(os.Stderr, "Removed %s\n", path)
				removed++
			} else {
				data, err := os.ReadFile(path)
				if err == nil && strings.Contains(string(data), "Generated by wikigen") {
					os.Remove(path)
					fmt.Fprintf(os.Stderr, "Removed %s\n", path)
					removed++
				}
			}
		}
		return nil
	})

	for _, name := range []string{"wiki.md", "index.html"} {
		p := filepath.Join(cfg.wikiRoot, name)
		if data, err := os.ReadFile(p); err == nil && strings.Contains(string(data), "wikigen") {
			os.Remove(p)
			fmt.Fprintf(os.Stderr, "Removed %s\n", p)
			removed++
		}
	}

	// Remove wikigen section from skill files in the repo root.
	for _, name := range []string{"CLAUDE.md", "AGENTS.md"} {
		p := filepath.Join(cfg.repoRoot, name)
		data, err := os.ReadFile(p)
		if err != nil {
			continue
		}
		content := string(data)
		startIdx := strings.Index(content, wikiSectionStart)
		endIdx := strings.Index(content, wikiSectionEnd)
		if startIdx < 0 || endIdx < 0 || endIdx <= startIdx {
			continue
		}
		// Remove the section and any surrounding blank lines.
		before := strings.TrimRight(content[:startIdx], "\n")
		after := strings.TrimLeft(content[endIdx+len(wikiSectionEnd):], "\n")
		cleaned := before
		if after != "" {
			if cleaned != "" {
				cleaned += "\n\n"
			}
			cleaned += after
		}
		if strings.TrimSpace(cleaned) == "" {
			// File was entirely the wikigen section — remove it.
			os.Remove(p)
			fmt.Fprintf(os.Stderr, "Removed %s\n", p)
		} else {
			if !strings.HasSuffix(cleaned, "\n") {
				cleaned += "\n"
			}
			os.WriteFile(p, []byte(cleaned), 0644)
			fmt.Fprintf(os.Stderr, "Removed wiki section from %s\n", p)
		}
		removed++
	}

	manifestPath := filepath.Join(cfg.wikiRoot, manifestFile)
	if err := os.Remove(manifestPath); err == nil {
		fmt.Fprintf(os.Stderr, "Removed %s\n", manifestPath)
		removed++
	}

	fmt.Fprintf(os.Stderr, "Cleaned %d generated files.\n", removed)
}
