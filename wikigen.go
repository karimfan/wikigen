func buildPrompt(b dirBundle) string {
	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("Directory: `%s`\n\n", b.Path))

	if len(b.Files) > 0 {
		sb.WriteString("## Source files in this directory\n\n")
		for _, f := range b.Files {
			sb.WriteString(fmt.Sprintf("### %s (%s)\n```%s\n%s\n```\n\n", f.Name, f.Language, strings.ToLower(f.Language), f.Content))
		}
	}

	if len(b.ChildSummaries) > 0 {
		sb.WriteString("## Child directory summaries\n\n")
		for i, cs := range b.ChildSummaries {
			if i < len(b.ChildDirs) {
				sb.WriteString(fmt.Sprintf("### Child: %s/\n%s\n\n", b.ChildDirs[i], cs))
			}
		}
	}

	return sb.String()
}

