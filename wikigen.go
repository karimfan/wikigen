func parseArgs() config {
	cfg := config{
		excludes: make(map[string]bool),
	}
	var userExcludes []string
	args := os.Args[1:]

	for i := 0; i < len(args); i++ {
		a := args[i]
		switch {
		case a == "--dry-run":
			cfg.dryRun = true
		case a == "--clean":
			cfg.clean = true
		case a == "--full":
			cfg.full = true
		case a == "--json":
			cfg.jsonProgress = true
		case a == "--no-default-excludes":
			cfg.noDefaultExcludes = true
		case a == "--no-deps-graph":
			cfg.noDepsGraph = true
		case a == "--no-file-index":
			cfg.noFileIndex = true
		case a == "--no-recipes":
			cfg.noRecipes = true
		case a == "--no-traces":
			cfg.noTraces = true
		case a == "--no-boundaries":
			cfg.noBoundaries = true
		case a == "--no-test-hints":
			cfg.noTestHints = true
		case a == "--no-symbol-index":
			cfg.noSymbolIndex = true
		case a == "--no-churn":
			cfg.noChurn = true
		case a == "--help" || a == "-h":
			printUsage()
			os.Exit(0)
		case a == "--output" && i+1 < len(args):
			i++
			cfg.wikiRoot = args[i]
		case a == "--exclude" && i+1 < len(args):
			i++
			userExcludes = append(userExcludes, args[i])
		case a == "--base-url" && i+1 < len(args):
			i++
			cfg.baseURL = strings.TrimRight(args[i], "/")
		case strings.HasPrefix(a, "-"):
			fmt.Fprintf(os.Stderr, "unknown flag: %s\n", a)
			printUsage()
			os.Exit(1)
		default:
			cfg.repoRoot = a
		}
	}

	if cfg.repoRoot == "" {
		cfg.repoRoot = "."
	}
	cfg.repoRoot, _ = filepath.Abs(cfg.repoRoot)

	if cfg.wikiRoot == "" {
		cfg.wikiRoot = cfg.repoRoot
	} else {
		cfg.wikiRoot, _ = filepath.Abs(cfg.wikiRoot)
	}

	// Build exclusion set.
	cfg.excludes[".git"] = true // always exclude .git
	if !cfg.noDefaultExcludes {
		for _, d := range defaultExcludes {
			cfg.excludes[d] = true
		}
	}
	for _, d := range userExcludes {
		cfg.excludes[d] = true
	}

	cfg.apiKey = os.Getenv("ANTHROPIC_API_KEY")

	return cfg
}

