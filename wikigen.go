func buildWiki(cfg config, dirs []string, summaries map[string]string, rootOverview string) string {
	var sb strings.Builder
	sb.WriteString(generatedTag + "\n")
	sb.WriteString("# Codebase Wiki\n\n")

	// Project overview from root-level LLM call.
	if rootOverview != "" {
		sb.WriteString(rootOverview)
		sb.WriteString("\n\n")
	} else {
		sb.WriteString("Hierarchical documentation of the project source code.\n\n")
	}

	var topLevel []string
	for _, d := range dirs {
		if !strings.Contains(d, string(os.PathSeparator)) {
			topLevel = append(topLevel, d)
		}
	}
	sort.Strings(topLevel)

	// Annotated contents — each entry gets a one-line description.
	sb.WriteString("## Contents\n\n")
	for _, d := range topLevel {
		desc := ""
		if s, ok := summaries[d]; ok {
			desc = extractFirstSentence(s)
		}
		if desc != "" {
			sb.WriteString(fmt.Sprintf("- [%s/](./%s/SUMMARY.md) — %s\n", d, d, desc))
		} else {
			sb.WriteString(fmt.Sprintf("- [%s/](./%s/SUMMARY.md)\n", d, d))
		}
	}
	sb.WriteString("\n---\n\n")

	for _, d := range topLevel {
		sb.WriteString(fmt.Sprintf("## %s\n\n", d))
		if s, ok := summaries[d]; ok {
			lines := strings.Split(s, "\n")
			var content []string
			for _, l := range lines {
				if strings.HasPrefix(l, "<!-- ") || (strings.HasPrefix(l, "# ") && !strings.HasPrefix(l, "## ")) {
					continue
				}
				content = append(content, l)
			}
			sb.WriteString(strings.TrimSpace(strings.Join(content, "\n")))
			sb.WriteString("\n\n")
		}
		sb.WriteString(fmt.Sprintf("[Full details →](./%s/SUMMARY.md)\n\n---\n\n", d))
	}

	return sb.String()
}

