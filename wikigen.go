// ---------------------------------------------------------------------------
// Directory collection (post-order)
// ---------------------------------------------------------------------------

func collectDirs(cfg config) ([]string, error) {
	var dirs []string
	err := filepath.Walk(cfg.repoRoot, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return nil
		}
		if !info.IsDir() {
			return nil
		}
		base := info.Name()
		if path != cfg.repoRoot && (strings.HasPrefix(base, ".") || cfg.excludes[base]) {
			return filepath.SkipDir
		}
		rel, _ := filepath.Rel(cfg.repoRoot, path)
		if rel == "." {
			return nil
		}
		dirs = append(dirs, rel)
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Slice(dirs, func(i, j int) bool {
		di := strings.Count(dirs[i], string(os.PathSeparator))
		dj := strings.Count(dirs[j], string(os.PathSeparator))
		if di != dj {
			return di > dj
		}
		return dirs[i] < dirs[j]
	})
	return dirs, nil
}

// ---------------------------------------------------------------------------
// Hashing
// ---------------------------------------------------------------------------

func hashBytes(data []byte) string {
	h := sha256.Sum256(data)
	return hex.EncodeToString(h[:])
}

func hashString(s string) string {
	return hashBytes([]byte(s))
}

func computeAllHashes(cfg config, dirs []string) map[string]manifestDirEntry {
	result := make(map[string]manifestDirEntry)
	for _, rel := range dirs {
		abs := filepath.Join(cfg.repoRoot, rel)
		entries, err := os.ReadDir(abs)
		if err != nil {
			continue
		}
		fileHashes := make(map[string]string)
		for _, e := range entries {
			if e.IsDir() {
				continue
			}
			name := e.Name()
			if !isEligibleFile(name) {
				continue
			}
			content, err := readFileContent(filepath.Join(abs, name))
			if err != nil || content == "" {
				continue
			}
			fileHashes[name] = hashString(content)
		}
		// Compute directory-level hash from sorted file hashes.
		var pairs []string
		for name, h := range fileHashes {
			pairs = append(pairs, name+":"+h)
		}
		sort.Strings(pairs)
		dirHash := hashString(strings.Join(pairs, "\n"))

		result[rel] = manifestDirEntry{
			FilesHash: dirHash,
			Files:     fileHashes,
		}
	}
	return result
}

// ---------------------------------------------------------------------------
// Manifest load/save
// ---------------------------------------------------------------------------

func loadManifest(cfg config) *manifest {
	path := filepath.Join(cfg.wikiRoot, manifestFile)
	data, err := os.ReadFile(path)
	if err != nil {
		return nil
	}
	var m manifest
	if err := json.Unmarshal(data, &m); err != nil {
		fmt.Fprintf(os.Stderr, "warning: corrupt manifest, will regenerate all: %v\n", err)
		return nil
	}
	return &m
}

func saveManifest(cfg config, m *manifest) {
	data, err := json.MarshalIndent(m, "", "  ")
	if err != nil {
		fmt.Fprintf(os.Stderr, "error marshaling manifest: %v\n", err)
		return
	}
	path := filepath.Join(cfg.wikiRoot, manifestFile)
	tmpPath := path + ".tmp"
	if err := os.WriteFile(tmpPath, data, 0644); err != nil {
		fmt.Fprintf(os.Stderr, "error writing manifest: %v\n", err)
		return
	}
	if err := os.Rename(tmpPath, path); err != nil {
		fmt.Fprintf(os.Stderr, "error renaming manifest: %v\n", err)
	}
}

func excludesMatch(a []string, b map[string]bool) bool {
	if len(a) != len(b) {
		return false
	}
	set := make(map[string]bool)
	for _, s := range a {
		set[s] = true
	}
	for k := range b {
		if !set[k] {
			return false
		}
	}
	return true
}

func sortedExcludes(m map[string]bool) []string {
	var s []string
	for k := range m {
		s = append(s, k)
	}
	sort.Strings(s)
	return s
}

