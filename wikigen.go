// stripLeadingH1 removes a leading "# ..." line from LLM output so we don't
// duplicate the header we add ourselves.
func stripLeadingH1(s string) string {
	s = strings.TrimSpace(s)
	if strings.HasPrefix(s, "# ") {
		if idx := strings.Index(s, "\n"); idx >= 0 {
			return strings.TrimSpace(s[idx+1:])
		}
		return "" // entire string was a single H1
	}
	return s
}

// extractFirstSentence pulls the first non-empty, non-heading line from a
// summary and returns it (stripped of markdown formatting) as a one-line
// description suitable for a table-of-contents entry.
func extractFirstSentence(summary string) string {
	for _, line := range strings.Split(summary, "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") || strings.HasPrefix(line, "<!--") || strings.HasPrefix(line, "---") {
			continue
		}
		// Strip bold/italic markers and leading list markers.
		line = strings.TrimLeft(line, "- *_")
		line = strings.TrimRight(line, " *_")
		// Truncate at first period if present.
		if idx := strings.Index(line, ". "); idx > 0 && idx < 200 {
			line = line[:idx+1]
		}
		if len(line) > 200 {
			line = line[:197] + "..."
		}
		return line
	}
	return ""
}

