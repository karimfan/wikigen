// ---------------------------------------------------------------------------
// Manifest types
// ---------------------------------------------------------------------------

type manifestDirEntry struct {
	FilesHash   string            `json:"files_hash"`
	SummaryHash string            `json:"summary_hash"`
	Files       map[string]string `json:"files"`
}

type manifest struct {
	Version     int                         `json:"version"`
	Repo        string                      `json:"repo"`
	LastCommit  string                      `json:"last_commit"`
	GeneratedAt string                      `json:"generated_at"`
	Excludes    []string                    `json:"excludes"`
	Directories map[string]manifestDirEntry `json:"directories"`
}

// ---------------------------------------------------------------------------
// JSON progress event
// ---------------------------------------------------------------------------

type progressEvent struct {
	Event        string `json:"event"`
	Dir          string `json:"dir,omitempty"`
	Status       string `json:"status,omitempty"`
	FilesChanged int    `json:"files_changed,omitempty"`
	Message      string `json:"message,omitempty"`
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

func main() {
	cfg := parseArgs()

	if cfg.clean {
		doClean(cfg)
		return
	}

	if !cfg.dryRun && cfg.apiKey == "" {
		fmt.Fprintln(os.Stderr, "error: ANTHROPIC_API_KEY environment variable is not set")
		fmt.Fprintln(os.Stderr, "Set it or use --dry-run to test without API calls.")
		os.Exit(1)
	}

	// 1. Collect directories in post-order.
	dirs, err := collectDirs(cfg)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error walking directories: %v\n", err)
		os.Exit(1)
	}

	// 2. Load manifest and detect changes.
	oldManifest := loadManifest(cfg)
	forceFullScan := cfg.full || oldManifest == nil || oldManifest.Version != manifestVersion || !excludesMatch(oldManifest.Excludes, cfg.excludes)

	// Compute current hashes for all directories.
	currentHashes := computeAllHashes(cfg, dirs)

	// Determine dirty set.
	dirty := make(map[string]bool)
	if forceFullScan {
		for _, d := range dirs {
			dirty[d] = true
		}
		if !cfg.full && oldManifest != nil && oldManifest.Version != manifestVersion {
			emitProgress(cfg, progressEvent{Event: "info", Message: "Manifest version changed, forcing full scan"})
		}
		if !cfg.full && oldManifest != nil && !excludesMatch(oldManifest.Excludes, cfg.excludes) {
			emitProgress(cfg, progressEvent{Event: "info", Message: "Exclusion list changed, forcing full scan"})
		}
	} else {
		// Try git diff first, fall back to hash comparison.
		dirty = detectChangesGit(cfg, oldManifest, dirs)
		if dirty == nil {
			dirty = detectChangesHash(oldManifest, currentHashes, dirs)
		}
	}

	// Propagate dirty upward.
	propagateDirty(dirty, dirs)

	// Handle deleted directories.
	if oldManifest != nil {
		handleDeletedDirs(cfg, oldManifest, dirs, dirty)
	}

	// Check if anything needs work.
	dirtyCount := 0
	for _, d := range dirs {
		if dirty[d] {
			dirtyCount++
		}
	}
	if dirtyCount == 0 {
		emitProgress(cfg, progressEvent{Event: "done", Message: fmt.Sprintf("No changes detected (%d directories)", len(dirs))})
		os.Exit(0)
	}

	// 3. Create wiki output directory structure.
	if cfg.wikiRoot != cfg.repoRoot {
		os.MkdirAll(cfg.wikiRoot, 0755)
	}

	// 4. Process directories: summarize dirty ones, load existing for clean ones.
	summaries := make(map[string]string)
	newManifest := &manifest{
		Version:     manifestVersion,
		Repo:        cfg.repoRoot,
		GeneratedAt: time.Now().UTC().Format(time.RFC3339),
		Excludes:    sortedExcludes(cfg.excludes),
		Directories: make(map[string]manifestDirEntry),
	}

	regenCount := 0
	skipCount := 0
	failCount := 0

	for _, rel := range dirs {
		if !dirty[rel] {
			// Load existing summary from wiki folder.
			existing := loadExistingSummary(cfg, rel)
			if existing != "" {
				summaries[rel] = existing
			}
			// Carry forward manifest entry.
			if oldManifest != nil {
				if entry, ok := oldManifest.Directories[rel]; ok {
					newManifest.Directories[rel] = entry
				}
			}
			// Ensure HTML exists even for skipped directories.
			if existing != "" && !cfg.dryRun {
				htmlPath := filepath.Join(cfg.wikiRoot, rel, "SUMMARY.html")
				if _, err := os.Stat(htmlPath); os.IsNotExist(err) {
					writeHTMLPage(htmlPath, filepath.Base(rel), existing, rel)
				}
			}
			emitProgress(cfg, progressEvent{Event: "skip", Dir: rel, Status: "unchanged"})
			skipCount++
			continue
		}

		bundle := buildBundle(cfg, rel, summaries)
		if len(bundle.Files) == 0 && len(bundle.ChildSummaries) == 0 {
			continue
		}

		nFiles := len(bundle.Files)
		nChildren := len(bundle.ChildSummaries)
		emitProgress(cfg, progressEvent{Event: "regenerate", Dir: rel, Status: "dirty", FilesChanged: nFiles})

		if !cfg.jsonProgress {
			fmt.Fprintf(os.Stderr, "Summarizing %s/ (%d files, %d children)...\n", rel, nFiles, nChildren)
		}

		if cfg.dryRun {
			summaries[rel] = fmt.Sprintf("(dry-run) %s: %d files, %d children", rel, nFiles, nChildren)
			continue
		}

		summary, err := summarize(cfg.apiKey, bundle)
		if err != nil {
			fmt.Fprintf(os.Stderr, "  warning: summarization failed for %s: %v\n", rel, err)
			summary = fmt.Sprintf("Summary generation failed for `%s`.", rel)
			failCount++
		}

		// Post-process links.
		summary = rewriteLinks(cfg, rel, summary)

		// Strip any leading H1 the LLM may have generated to avoid duplicates.
		summary = stripLeadingH1(summary)

		md := fmt.Sprintf("%s\n# %s\n\n%s\n", generatedTag, filepath.Base(rel), summary)
		summaries[rel] = md

		// Write SUMMARY.md to wiki folder.
		outDir := filepath.Join(cfg.wikiRoot, rel)
		os.MkdirAll(outDir, 0755)
		outPath := filepath.Join(outDir, "SUMMARY.md")
		if err := os.WriteFile(outPath, []byte(md), 0644); err != nil {
			fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", outPath, err)
		}

		// Write SUMMARY.html alongside the markdown.
		htmlPath := filepath.Join(outDir, "SUMMARY.html")
		if err := writeHTMLPage(htmlPath, filepath.Base(rel), md, rel); err != nil {
			fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", htmlPath, err)
		}

		// Update manifest entry.
		if ch, ok := currentHashes[rel]; ok {
			ch.SummaryHash = hashString(md)
			newManifest.Directories[rel] = ch
		}
		regenCount++
	}

	// 5. Generate root overview and wiki.md.
	if !cfg.dryRun {
		// Collect top-level dirs for root overview.
		var topLevel []string
		for _, d := range dirs {
			if !strings.Contains(d, string(os.PathSeparator)) {
				topLevel = append(topLevel, d)
			}
		}
		sort.Strings(topLevel)

		emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "Generating project overview"})
		if !cfg.jsonProgress {
			fmt.Fprintln(os.Stderr, "Generating project overview...")
		}
		rootOverview, err := summarizeRoot(cfg.apiKey, topLevel, summaries)
		if err != nil {
			fmt.Fprintf(os.Stderr, "  warning: root overview generation failed: %v\n", err)
			rootOverview = ""
		}

		emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "Generating wiki.md"})
		if !cfg.jsonProgress {
			fmt.Fprintln(os.Stderr, "Generating wiki.md...")
		}
		wiki := buildWiki(cfg, dirs, summaries, rootOverview)
		wikiPath := filepath.Join(cfg.wikiRoot, "wiki.md")
		if err := os.WriteFile(wikiPath, []byte(wiki), 0644); err != nil {
			fmt.Fprintf(os.Stderr, "error writing wiki.md: %v\n", err)
			os.Exit(1)
		}

		// Write index.html alongside wiki.md.
		indexPath := filepath.Join(cfg.wikiRoot, "index.html")
		if err := writeHTMLPage(indexPath, "Codebase Wiki", wiki, ""); err != nil {
			fmt.Fprintf(os.Stderr, "error writing index.html: %v\n", err)
		}

		// Write LLM skill files (CLAUDE.md, AGENTS.md) into the wiki output.
		writeSkillFiles(cfg)
	} else {
		emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "(dry-run) Would write wiki.md"})
		if !cfg.jsonProgress {
			fmt.Fprintln(os.Stderr, "(dry-run) Would write wiki.md")
		}
	}

	// 6. Save manifest.
	if !cfg.dryRun {
		// Get current git HEAD.
		newManifest.LastCommit = getGitHead(cfg.repoRoot)
		saveManifest(cfg, newManifest)
	}

	// 7. Print stats.
	total := len(dirs)
	msg := fmt.Sprintf("Regenerated %d/%d directories (%d skipped)", regenCount, total, skipCount)
	if failCount > 0 {
		msg += fmt.Sprintf(", %d failed", failCount)
	}
	emitProgress(cfg, progressEvent{Event: "done", Message: msg})
	if !cfg.jsonProgress {
		fmt.Fprintln(os.Stderr, msg)
	}

	if failCount > 0 {
		os.Exit(2) // partial failure
	}
}

// ---------------------------------------------------------------------------
// Argument parsing
// ---------------------------------------------------------------------------

func parseArgs() config {
	cfg := config{
		excludes: make(map[string]bool),
	}
	var userExcludes []string
	args := os.Args[1:]

	for i := 0; i < len(args); i++ {
		a := args[i]
		switch {
		case a == "--dry-run":
			cfg.dryRun = true
		case a == "--clean":
			cfg.clean = true
		case a == "--full":
			cfg.full = true
		case a == "--json":
			cfg.jsonProgress = true
		case a == "--no-default-excludes":
			cfg.noDefaultExcludes = true
		case a == "--help" || a == "-h":
			printUsage()
			os.Exit(0)
		case a == "--output" && i+1 < len(args):
			i++
			cfg.wikiRoot = args[i]
		case a == "--exclude" && i+1 < len(args):
			i++
			userExcludes = append(userExcludes, args[i])
		case a == "--base-url" && i+1 < len(args):
			i++
			cfg.baseURL = strings.TrimRight(args[i], "/")
		case strings.HasPrefix(a, "-"):
			fmt.Fprintf(os.Stderr, "unknown flag: %s\n", a)
			printUsage()
			os.Exit(1)
		default:
			cfg.repoRoot = a
		}
	}

	if cfg.repoRoot == "" {
		cfg.repoRoot = "."
	}
	cfg.repoRoot, _ = filepath.Abs(cfg.repoRoot)

	if cfg.wikiRoot == "" {
		cfg.wikiRoot = cfg.repoRoot
	} else {
		cfg.wikiRoot, _ = filepath.Abs(cfg.wikiRoot)
	}

	// Build exclusion set.
	cfg.excludes[".git"] = true // always exclude .git
	if !cfg.noDefaultExcludes {
		for _, d := range defaultExcludes {
			cfg.excludes[d] = true
		}
	}
	for _, d := range userExcludes {
		cfg.excludes[d] = true
	}

	cfg.apiKey = os.Getenv("ANTHROPIC_API_KEY")

	return cfg
}

func printUsage() {
	fmt.Fprintln(os.Stderr, `Usage: wikigen [flags] <repo>

Generates hierarchical markdown documentation for a codebase.

Flags:
  --output <dir>       Wiki output directory (default: in-place in repo)
  --exclude <pattern>  Exclude directory by base name (repeatable)
  --no-default-excludes  Clear default exclusions (only .git remains)
  --base-url <url>     URL prefix for source file links (e.g. https://github.com/org/repo/blob/main)
  --full               Force full regeneration (ignore manifest)
  --json               Emit line-delimited JSON progress events on stderr
  --dry-run            Show what would be summarized without calling the API
  --clean              Remove all generated files and manifest from wiki folder
  --help               Show this help message`)
}

// ---------------------------------------------------------------------------
// Directory collection (post-order)
// ---------------------------------------------------------------------------

func collectDirs(cfg config) ([]string, error) {
	var dirs []string
	err := filepath.Walk(cfg.repoRoot, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return nil
		}
		if !info.IsDir() {
			return nil
		}
		base := info.Name()
		if path != cfg.repoRoot && (strings.HasPrefix(base, ".") || cfg.excludes[base]) {
			return filepath.SkipDir
		}
		rel, _ := filepath.Rel(cfg.repoRoot, path)
		if rel == "." {
			return nil
		}
		dirs = append(dirs, rel)
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Slice(dirs, func(i, j int) bool {
		di := strings.Count(dirs[i], string(os.PathSeparator))
		dj := strings.Count(dirs[j], string(os.PathSeparator))
		if di != dj {
			return di > dj
		}
		return dirs[i] < dirs[j]
	})
	return dirs, nil
}

// ---------------------------------------------------------------------------
// Hashing
// ---------------------------------------------------------------------------

func hashBytes(data []byte) string {
	h := sha256.Sum256(data)
	return hex.EncodeToString(h[:])
}

func hashString(s string) string {
	return hashBytes([]byte(s))
}

func computeAllHashes(cfg config, dirs []string) map[string]manifestDirEntry {
	result := make(map[string]manifestDirEntry)
	for _, rel := range dirs {
		abs := filepath.Join(cfg.repoRoot, rel)
		entries, err := os.ReadDir(abs)
		if err != nil {
			continue
		}
		fileHashes := make(map[string]string)
		for _, e := range entries {
			if e.IsDir() {
				continue
			}
			name := e.Name()
			if !isEligibleFile(name) {
				continue
			}
			content, err := readFileContent(filepath.Join(abs, name))
			if err != nil || content == "" {
				continue
			}
			fileHashes[name] = hashString(content)
		}
		// Compute directory-level hash from sorted file hashes.
		var pairs []string
		for name, h := range fileHashes {
			pairs = append(pairs, name+":"+h)
		}
		sort.Strings(pairs)
		dirHash := hashString(strings.Join(pairs, "\n"))

		result[rel] = manifestDirEntry{
			FilesHash: dirHash,
			Files:     fileHashes,
		}
	}
	return result
}

// ---------------------------------------------------------------------------
// Manifest load/save
// ---------------------------------------------------------------------------

func loadManifest(cfg config) *manifest {
	path := filepath.Join(cfg.wikiRoot, manifestFile)
	data, err := os.ReadFile(path)
	if err != nil {
		return nil
	}
	var m manifest
	if err := json.Unmarshal(data, &m); err != nil {
		fmt.Fprintf(os.Stderr, "warning: corrupt manifest, will regenerate all: %v\n", err)
		return nil
	}
	return &m
}

func saveManifest(cfg config, m *manifest) {
	data, err := json.MarshalIndent(m, "", "  ")
	if err != nil {
		fmt.Fprintf(os.Stderr, "error marshaling manifest: %v\n", err)
		return
	}
	path := filepath.Join(cfg.wikiRoot, manifestFile)
	tmpPath := path + ".tmp"
	if err := os.WriteFile(tmpPath, data, 0644); err != nil {
		fmt.Fprintf(os.Stderr, "error writing manifest: %v\n", err)
		return
	}
	if err := os.Rename(tmpPath, path); err != nil {
		fmt.Fprintf(os.Stderr, "error renaming manifest: %v\n", err)
	}
}

func excludesMatch(a []string, b map[string]bool) bool {
	if len(a) != len(b) {
		return false
	}
	set := make(map[string]bool)
	for _, s := range a {
		set[s] = true
	}
	for k := range b {
		if !set[k] {
			return false
		}
	}
	return true
}

func sortedExcludes(m map[string]bool) []string {
	var s []string
	for k := range m {
		s = append(s, k)
	}
	sort.Strings(s)
	return s
}

// ---------------------------------------------------------------------------
// Change detection: git
// ---------------------------------------------------------------------------

func getGitHead(repoRoot string) string {
	cmd := exec.Command("git", "rev-parse", "HEAD")
	cmd.Dir = repoRoot
	out, err := cmd.Output()
	if err != nil {
		return ""
	}
	return strings.TrimSpace(string(out))
}

func isGitRepo(repoRoot string) bool {
	info, err := os.Stat(filepath.Join(repoRoot, ".git"))
	return err == nil && info.IsDir()
}

func detectChangesGit(cfg config, old *manifest, dirs []string) map[string]bool {
	if !isGitRepo(cfg.repoRoot) || old.LastCommit == "" {
		return nil // fall back to hash
	}

	currentHead := getGitHead(cfg.repoRoot)
	if currentHead == "" {
		return nil
	}
	if currentHead == old.LastCommit {
		// Same commit — no changes from git's perspective.
		// Still need to check for new/removed dirs via hash comparison.
		return detectChangesHash(old, computeAllHashes(cfg, dirs), dirs)
	}

	cmd := exec.Command("git", "diff", "--name-only", old.LastCommit, currentHead)
	cmd.Dir = cfg.repoRoot
	out, err := cmd.Output()
	if err != nil {
		emitProgress(cfg, progressEvent{Event: "info", Message: "git diff failed, falling back to hash comparison"})
		return nil
	}

	dirty := make(map[string]bool)
	lines := strings.Split(strings.TrimSpace(string(out)), "\n")
	dirSet := make(map[string]bool)
	for _, d := range dirs {
		dirSet[d] = true
	}

	for _, line := range lines {
		if line == "" {
			continue
		}
		dir := filepath.Dir(line)
		// Walk up to find the first eligible directory.
		for dir != "." && dir != "" {
			if dirSet[dir] {
				dirty[dir] = true
				break
			}
			dir = filepath.Dir(dir)
		}
	}

	// Also check for new directories (not in manifest).
	for _, d := range dirs {
		if _, exists := old.Directories[d]; !exists {
			dirty[d] = true
		}
	}

	return dirty
}

// ---------------------------------------------------------------------------
// Change detection: hash fallback
// ---------------------------------------------------------------------------

func detectChangesHash(old *manifest, current map[string]manifestDirEntry, dirs []string) map[string]bool {
	dirty := make(map[string]bool)
	for _, d := range dirs {
		cur, hasCur := current[d]
		oldEntry, hasOld := old.Directories[d]
		if !hasOld {
			dirty[d] = true // new directory
			continue
		}
		if !hasCur {
			continue
		}
		if cur.FilesHash != oldEntry.FilesHash {
			dirty[d] = true
		}
	}
	return dirty
}

// ---------------------------------------------------------------------------
// Dirty propagation
// ---------------------------------------------------------------------------

func propagateDirty(dirty map[string]bool, dirs []string) {
	// Process in reverse (shallowest first) to propagate upward.
	for i := len(dirs) - 1; i >= 0; i-- {
		d := dirs[i]
		if !dirty[d] {
			continue
		}
		parent := filepath.Dir(d)
		for parent != "." && parent != "" {
			dirty[parent] = true
			parent = filepath.Dir(parent)
		}
	}
}

// ---------------------------------------------------------------------------
// Deleted directory handling
// ---------------------------------------------------------------------------

func handleDeletedDirs(cfg config, old *manifest, dirs []string, dirty map[string]bool) {
	currentSet := make(map[string]bool)
	for _, d := range dirs {
		currentSet[d] = true
	}
	for d := range old.Directories {
		if !currentSet[d] {
			// Directory was removed from repo.
			summaryPath := filepath.Join(cfg.wikiRoot, d, "SUMMARY.md")
			if err := os.Remove(summaryPath); err == nil {
				emitProgress(cfg, progressEvent{Event: "delete", Dir: d, Message: "Removed deleted directory summary"})
			}
			// Mark parent as dirty.
			parent := filepath.Dir(d)
			if parent != "." {
				dirty[parent] = true
			}
		}
	}
}

// ---------------------------------------------------------------------------
// Bundle building
// ---------------------------------------------------------------------------

func buildBundle(cfg config, rel string, summaries map[string]string) dirBundle {
	abs := filepath.Join(cfg.repoRoot, rel)
	entries, _ := os.ReadDir(abs)

	b := dirBundle{Path: rel}

	for _, e := range entries {
		if e.IsDir() {
			childRel := filepath.Join(rel, e.Name())
			if s, ok := summaries[childRel]; ok {
				b.ChildSummaries = append(b.ChildSummaries, s)
				b.ChildDirs = append(b.ChildDirs, e.Name())
			}
			continue
		}
		name := e.Name()
		if !isEligibleFile(name) {
			continue
		}
		lang := detectLanguage(name)
		if lang == "" {
			continue
		}
		content, err := readFileContent(filepath.Join(abs, name))
		if err != nil || content == "" {
			continue
		}
		b.Files = append(b.Files, fileEntry{Name: name, Language: lang, Content: content})
	}
	sort.Slice(b.Files, func(i, j int) bool {
		return b.Files[i].Name < b.Files[j].Name
	})
	return b
}

func isEligibleFile(name string) bool {
	if skipFiles[name] || name == "SUMMARY.md" || name == manifestFile {
		return false
	}
	if detectLanguage(name) == "" {
		return false
	}
	return true
}

func detectLanguage(name string) string {
	for prefix, lang := range specialFiles {
		if name == prefix || strings.HasPrefix(name, prefix+".") {
			return lang
		}
	}
	ext := filepath.Ext(name)
	return extLang[ext]
}

func readFileContent(path string) (string, error) {
	info, err := os.Stat(path)
	if err != nil {
		return "", err
	}
	if info.Size() > maxFileBytes {
		return "", nil
	}

	data, err := os.ReadFile(path)
	if err != nil {
		return "", err
	}

	check := data
	if len(check) > 512 {
		check = check[:512]
	}
	if bytes.ContainsRune(check, 0) {
		return "", nil
	}

	header := string(check)
	if strings.Contains(header, "Code generated") || strings.Contains(header, "DO NOT EDIT") {
		return "", nil
	}

	lines := strings.SplitN(string(data), "\n", maxFileLines+1)
	if len(lines) > maxFileLines {
		lines = lines[:maxFileLines]
		lines = append(lines, fmt.Sprintf("\n... (truncated at %d lines)", maxFileLines))
	}
	return strings.Join(lines, "\n"), nil
}

