// markdownToHTML converts markdown text to an HTML body fragment.
func markdownToHTML(md string) string {
	// Strip the wikigen comment tag before converting.
	md = strings.ReplaceAll(md, generatedTag, "")
	var buf bytes.Buffer
	if err := goldmark.Convert([]byte(md), &buf); err != nil {
		return "<p>Error rendering markdown.</p>"
	}
	return buf.String()
}

// rewriteMDLinksToHTML rewrites .md hrefs to .html in the HTML output so
// navigation works in a browser.
var mdLinkInHTML = regexp.MustCompile(`href="([^"]*?)\.md"`)

func rewriteHTMLLinks(html string) string {
	return mdLinkInHTML.ReplaceAllString(html, `href="$1.html"`)
}

// buildBreadcrumb creates an HTML breadcrumb trail for a directory path.
// rel is relative to the wiki root (e.g., "internal/lsm").
func buildBreadcrumb(rel string) template.HTML {
	if rel == "" || rel == "." {
		return ""
	}
	parts := strings.Split(rel, string(os.PathSeparator))
	var crumbs []string
	// Link to root wiki.
	depth := len(parts)
	rootHref := strings.Repeat("../", depth) + "index.html"
	crumbs = append(crumbs, fmt.Sprintf(`<a href="%s">Wiki</a>`, rootHref))
	// Intermediate dirs.
	for i, p := range parts {
		if i == len(parts)-1 {
			crumbs = append(crumbs, p)
		} else {
			ups := len(parts) - i - 1
			href := strings.Repeat("../", ups) + "SUMMARY.html"
			crumbs = append(crumbs, fmt.Sprintf(`<a href="%s">%s</a>`, href, p))
		}
	}
	return template.HTML(strings.Join(crumbs, " / "))
}

// writeHTMLPage renders a markdown string as a styled HTML page.
func writeHTMLPage(outPath, title, mdContent, rel string) error {
	body := markdownToHTML(mdContent)
	body = rewriteHTMLLinks(body)

	page := htmlPage{
		Title:      title + " — Codebase Wiki",
		Breadcrumb: buildBreadcrumb(rel),
		Body:       template.HTML(body),
	}

	var buf bytes.Buffer
	if err := htmlTemplate.Execute(&buf, page); err != nil {
		return fmt.Errorf("render HTML template: %w", err)
	}
	return os.WriteFile(outPath, buf.Bytes(), 0644)
}

