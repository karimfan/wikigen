type manifestDirEntry struct {
	FilesHash   string            `json:"files_hash"`
	SummaryHash string            `json:"summary_hash"`
	Files       map[string]string `json:"files"`
}

type manifest struct {
	Version     int                         `json:"version"`
	Repo        string                      `json:"repo"`
	LastCommit  string                      `json:"last_commit"`
	GeneratedAt string                      `json:"generated_at"`
	Excludes    []string                    `json:"excludes"`
	Directories map[string]manifestDirEntry `json:"directories"`
}

// ---------------------------------------------------------------------------
// JSON progress event
// ---------------------------------------------------------------------------

type progressEvent struct {
	Event        string `json:"event"`
	Dir          string `json:"dir,omitempty"`
	Status       string `json:"status,omitempty"`
	FilesChanged int    `json:"files_changed,omitempty"`
	Message      string `json:"message,omitempty"`
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

func main() {
	cfg := parseArgs()

	if cfg.clean {
		doClean(cfg)
		return
	}

	if !cfg.dryRun && cfg.apiKey == "" {
		fmt.Fprintln(os.Stderr, "error: ANTHROPIC_API_KEY environment variable is not set")
		fmt.Fprintln(os.Stderr, "Set it or use --dry-run to test without API calls.")
		os.Exit(1)
	}

	// 1. Collect directories in post-order.
	dirs, err := collectDirs(cfg)
	if err != nil {
		fmt.Fprintf(os.Stderr, "error walking directories: %v\n", err)
		os.Exit(1)
	}

	// 2. Load manifest and detect changes.
	oldManifest := loadManifest(cfg)
	forceFullScan := cfg.full || oldManifest == nil || oldManifest.Version != manifestVersion || !excludesMatch(oldManifest.Excludes, cfg.excludes)

	// Compute current hashes for all directories.
	currentHashes := computeAllHashes(cfg, dirs)

	// Determine dirty set.
	dirty := make(map[string]bool)
	if forceFullScan {
		for _, d := range dirs {
			dirty[d] = true
		}
		if !cfg.full && oldManifest != nil && oldManifest.Version != manifestVersion {
			emitProgress(cfg, progressEvent{Event: "info", Message: "Manifest version changed, forcing full scan"})
		}
		if !cfg.full && oldManifest != nil && !excludesMatch(oldManifest.Excludes, cfg.excludes) {
			emitProgress(cfg, progressEvent{Event: "info", Message: "Exclusion list changed, forcing full scan"})
		}
	} else {
		// Try git diff first, fall back to hash comparison.
		dirty = detectChangesGit(cfg, oldManifest, dirs)
		if dirty == nil {
			dirty = detectChangesHash(oldManifest, currentHashes, dirs)
		}
	}

	// Propagate dirty upward.
	propagateDirty(dirty, dirs)

	// Handle deleted directories.
	if oldManifest != nil {
		handleDeletedDirs(cfg, oldManifest, dirs, dirty)
	}

	// Check if anything needs work.
	dirtyCount := 0
	for _, d := range dirs {
		if dirty[d] {
			dirtyCount++
		}
	}
	if dirtyCount == 0 {
		emitProgress(cfg, progressEvent{Event: "done", Message: fmt.Sprintf("No changes detected (%d directories)", len(dirs))})
		os.Exit(0)
	}

	// 3. Create wiki output directory structure.
	if cfg.wikiRoot != cfg.repoRoot {
		os.MkdirAll(cfg.wikiRoot, 0755)
	}

	// Build parent→children map for child navigation in HTML.
	parentChildren := make(map[string][]string)
	for _, rel := range dirs {
		parent := filepath.Dir(rel)
		if parent == "." {
			parent = ""
		}
		parentChildren[parent] = append(parentChildren[parent], filepath.Base(rel))
	}
	for k := range parentChildren {
		sort.Strings(parentChildren[k])
	}

	// 4. Process directories: summarize dirty ones, load existing for clean ones.
	summaries := make(map[string]string)
	newManifest := &manifest{
		Version:     manifestVersion,
		Repo:        cfg.repoRoot,
		GeneratedAt: time.Now().UTC().Format(time.RFC3339),
		Excludes:    sortedExcludes(cfg.excludes),
		Directories: make(map[string]manifestDirEntry),
	}

	regenCount := 0
	skipCount := 0
	failCount := 0

	for _, rel := range dirs {
		if !dirty[rel] {
			// Load existing summary from wiki folder.
			existing := loadExistingSummary(cfg, rel)
			if existing != "" {
				summaries[rel] = existing
			}
			// Carry forward manifest entry.
			if oldManifest != nil {
				if entry, ok := oldManifest.Directories[rel]; ok {
					newManifest.Directories[rel] = entry
				}
			}
			// Ensure HTML exists even for skipped directories.
			if existing != "" && !cfg.dryRun {
				htmlPath := filepath.Join(cfg.wikiRoot, rel, "SUMMARY.html")
				if _, err := os.Stat(htmlPath); os.IsNotExist(err) {
					writeHTMLPage(htmlPath, filepath.Base(rel), existing, rel, parentChildren[rel])
				}
			}
			emitProgress(cfg, progressEvent{Event: "skip", Dir: rel, Status: "unchanged"})
			skipCount++
			continue
		}

		bundle := buildBundle(cfg, rel, summaries)
		if len(bundle.Files) == 0 && len(bundle.ChildSummaries) == 0 {
			continue
		}

		nFiles := len(bundle.Files)
		nChildren := len(bundle.ChildSummaries)
		emitProgress(cfg, progressEvent{Event: "regenerate", Dir: rel, Status: "dirty", FilesChanged: nFiles})

		if !cfg.jsonProgress {
			fmt.Fprintf(os.Stderr, "Summarizing %s/ (%d files, %d children)...\n", rel, nFiles, nChildren)
		}

		if cfg.dryRun {
			summaries[rel] = fmt.Sprintf("(dry-run) %s: %d files, %d children", rel, nFiles, nChildren)
			continue
		}

		summary, err := summarize(cfg, bundle)
		if err != nil {
			fmt.Fprintf(os.Stderr, "  warning: summarization failed for %s: %v\n", rel, err)
			summary = fmt.Sprintf("Summary generation failed for `%s`.", rel)
			failCount++
		}

		// Post-process links.
		summary = rewriteLinks(cfg, rel, summary)

		// Strip any leading H1 the LLM may have generated to avoid duplicates.
		summary = stripLeadingH1(summary)

		md := fmt.Sprintf("%s\n# %s\n\n%s\n", generatedTag, filepath.Base(rel), summary)
		summaries[rel] = md

		// Write SUMMARY.md to wiki folder.
		outDir := filepath.Join(cfg.wikiRoot, rel)
		os.MkdirAll(outDir, 0755)
		outPath := filepath.Join(outDir, "SUMMARY.md")
		if err := os.WriteFile(outPath, []byte(md), 0644); err != nil {
			fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", outPath, err)
		}

		// Write SUMMARY.html alongside the markdown.
		htmlPath := filepath.Join(outDir, "SUMMARY.html")
		if err := writeHTMLPage(htmlPath, filepath.Base(rel), md, rel, parentChildren[rel]); err != nil {
			fmt.Fprintf(os.Stderr, "  error writing %s: %v\n", htmlPath, err)
		}

		// Update manifest entry.
		if ch, ok := currentHashes[rel]; ok {
			ch.SummaryHash = hashString(md)
			newManifest.Directories[rel] = ch
		}
		regenCount++
	}

	// --- Sprint 005: Post-processing pipeline ---
	var artifacts wikiArtifacts
	artifacts.dirCount = len(dirs)

	if cfg.dryRun {
		// Print what would be generated.
		if !cfg.noDepsGraph {
			fmt.Fprintln(os.Stderr, "(dry-run) Would generate deps-graph.md")
		}
		if !cfg.noFileIndex {
			fmt.Fprintln(os.Stderr, "(dry-run) Would generate file-index.md")
		}
		if !cfg.noSymbolIndex {
			fmt.Fprintln(os.Stderr, "(dry-run) Would generate symbol-index.md")
		}
		if !cfg.noChurn {
			fmt.Fprintln(os.Stderr, "(dry-run) Would generate churn.md")
		}
		if !cfg.noRecipes {
			fmt.Fprintln(os.Stderr, "(dry-run) Would generate recipes.md (1 LLM call)")
		}
		if !cfg.noTraces {
			fmt.Fprintln(os.Stderr, "(dry-run) Would generate traces.md (1 LLM call)")
		}
	}

	if !cfg.dryRun {
		// Write dependency graph.
		if !cfg.noDepsGraph {
			edgeCount, err := writeDepsGraph(cfg, summaries)
			if err != nil {
				fmt.Fprintf(os.Stderr, "  warning: deps graph generation failed: %v\n", err)
			}
			artifacts.depsEdgeCount = edgeCount
		}

		// Write file index.
		if !cfg.noFileIndex {
			fileCount, err := writeFileIndex(cfg, dirs, summaries)
			if err != nil {
				fmt.Fprintf(os.Stderr, "  warning: file index generation failed: %v\n", err)
			}
			artifacts.fileCount = fileCount
		}

		// Write symbol index.
		if !cfg.noSymbolIndex {
			symbolCount, err := writeSymbolIndex(cfg, summaries)
			if err != nil {
				fmt.Fprintf(os.Stderr, "  warning: symbol index generation failed: %v\n", err)
			}
			artifacts.symbolCount = symbolCount
		}

		// Git analysis: churn.
		if !cfg.noChurn {
			churnData, _ := analyzeChurn(cfg.repoRoot)
			if len(churnData) > 0 {
				if err := writeChurn(cfg, churnData); err != nil {
					fmt.Fprintf(os.Stderr, "  warning: churn analysis failed: %v\n", err)
				}
				artifacts.hasChurn = true
			}
		}

		// Git analysis: co-change + recipes.
		if !cfg.noRecipes {
			clusters, _ := analyzeCoChanges(cfg.repoRoot)
			if len(clusters) > 0 {
				emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "Generating modification recipes"})
				if !cfg.jsonProgress {
					fmt.Fprintln(os.Stderr, "Generating modification recipes...")
				}
				recipesContent, err := generateRecipes(cfg, clusters, summaries)
				if err != nil {
					fmt.Fprintf(os.Stderr, "  warning: recipe generation failed: %v\n", err)
				} else if recipesContent != "" {
					writeRecipesFile(cfg, recipesContent)
					artifacts.hasRecipes = true
				}
			}
		}

		// Entry point traces.
		if !cfg.noTraces {
			emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "Generating entry point traces"})
			if !cfg.jsonProgress {
				fmt.Fprintln(os.Stderr, "Generating entry point traces...")
			}
			tracesContent, err := generateTraces(cfg, summaries)
			if err != nil {
				fmt.Fprintf(os.Stderr, "  warning: trace generation failed: %v\n", err)
			} else if tracesContent != "" {
				writeTracesFile(cfg, tracesContent)
				artifacts.hasTraces = true
			}
		}
	}

	// 5. Generate root overview and wiki.md.
	if !cfg.dryRun {
		// Collect top-level dirs for root overview.
		var topLevel []string
		for _, d := range dirs {
			if !strings.Contains(d, string(os.PathSeparator)) {
				topLevel = append(topLevel, d)
			}
		}
		sort.Strings(topLevel)

		emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "Generating project overview"})
		if !cfg.jsonProgress {
			fmt.Fprintln(os.Stderr, "Generating project overview...")
		}
		rootOverview, err := summarizeRoot(cfg.apiKey, topLevel, summaries)
		if err != nil {
			fmt.Fprintf(os.Stderr, "  warning: root overview generation failed: %v\n", err)
			rootOverview = ""
		}

		emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "Generating wiki.md"})
		if !cfg.jsonProgress {
			fmt.Fprintln(os.Stderr, "Generating wiki.md...")
		}
		wiki := buildWiki(cfg, dirs, summaries, rootOverview, artifacts)
		wikiPath := filepath.Join(cfg.wikiRoot, "wiki.md")
		if err := os.WriteFile(wikiPath, []byte(wiki), 0644); err != nil {
			fmt.Fprintf(os.Stderr, "error writing wiki.md: %v\n", err)
			os.Exit(1)
		}

		// Write index.html alongside wiki.md.
		indexPath := filepath.Join(cfg.wikiRoot, "index.html")
		if err := writeHTMLPage(indexPath, "Codebase Wiki", wiki, "", parentChildren[""]); err != nil {
			fmt.Fprintf(os.Stderr, "error writing index.html: %v\n", err)
		}

		// Write LLM skill files (CLAUDE.md, AGENTS.md) into the wiki output.
		writeSkillFiles(cfg)
	} else {
		emitProgress(cfg, progressEvent{Event: "write", Dir: ".", Message: "(dry-run) Would write wiki.md"})
		if !cfg.jsonProgress {
			fmt.Fprintln(os.Stderr, "(dry-run) Would write wiki.md")
		}
	}

	// 6. Save manifest.
	if !cfg.dryRun {
		// Get current git HEAD.
		newManifest.LastCommit = getGitHead(cfg.repoRoot)
		saveManifest(cfg, newManifest)
	}

	// 7. Print stats.
	total := len(dirs)
	msg := fmt.Sprintf("Regenerated %d/%d directories (%d skipped)", regenCount, total, skipCount)
	if failCount > 0 {
		msg += fmt.Sprintf(", %d failed", failCount)
	}
	emitProgress(cfg, progressEvent{Event: "done", Message: msg})
	if !cfg.jsonProgress {
		fmt.Fprintln(os.Stderr, msg)
	}

	if failCount > 0 {
		os.Exit(2) // partial failure
	}
}

